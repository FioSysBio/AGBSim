package br.fiocruz.procc.acbmservice.domain;

public class Coordinates {
	
    private int x, y, z;

	public Coordinates(int x, int y, int z) {
		super();
		this.x = x;
		this.y = y;
		this.z = z;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public int getZ() {
		return z;
	}

}
